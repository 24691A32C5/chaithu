SET SERVEROUTPUT.ON
DECLARE
V_EMPNO EMP_O3.EMPNO%TYPE:=&ENTER_EMPNO;
V_SAL EMP_O3.SAL%TYPE:=&NEW_SAL;
CURSOR CURSOR_EMP_O3(V_EMPNO NUMBER)IS
SELECT EMPNO,ENAME,SAL
FROM EMP_O3
WHERE EMPNO=V_EMPNO
FOR UPDATE OF SAL;
V_EMP_O3 CURSOR_EMP_O3%ROWTYPE;
BEGIN
OPEN CURSOR_EMP_O3(V_EMPNO);
FETCH CURSOR_EMP_O3 INTO V_EMP_O3;
IF CURSOR_EMP_O3% FOUND THEN 
DBMS_OUTPUT.PUT_LINE('---BEFORE UPDATE--');
DBMS_OUTPUT.PUT_LINE('EMPLOYEE NAME'||V_EMP_O3.EMPNO);
DBMS_OUTPUT.PUT_LINE('OLD SALARY '||V_EMP_O3.SAL);
UPDATE EMP_O3
SET SAL=V_SAL
WHERE EMPNO=V_EMPNO;
DBMS_OUTPUT.PUT_LINE('---AFTER UPDATE---');
DBMS_OUTPUT.PUT_LINE('EMPLOYEE NAME'||V_EMP_O3.EMPNO);
DBMS_OUTPUT.PUT_LINE('NEW SALARY'||V_EMP_O3.SAL);
ELSE
DBMS_OUTPUT.PUT_LINE('NO EMPLYOEE FOUND WITH ID'||V_EMP_O3.EMPNO);
END IF;
CLOSE CURSOR_EMP_O3;
COMMIT;
END;
/